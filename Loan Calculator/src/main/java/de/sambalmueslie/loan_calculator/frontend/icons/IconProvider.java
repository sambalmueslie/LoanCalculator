	/** the {@link Logger}. */
	private static Logger logger = LogManager.getLogger(IconProvider.class);

	/**
	 * Create a {@link ImageView}.
	 *
	 * @param imageName
	 *            the image name.
	 * @return the view.
	 */
	public static ImageView createImageView(final String imageName) {
		try {
			return new ImageView(new Image(IconProvider.class.getResourceAsStream(imageName)));
		} catch (final RuntimeException e) {
			logger.error("Cannot create image '" + imageName + "' cause : " + e.getMessage());
			return null;
		}
	}

